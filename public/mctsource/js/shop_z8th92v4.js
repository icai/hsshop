$(function(){
	// 认证显示与隐藏
	$('.cotent_items li').hover(function(){				// 显示
		$(this).find('.cotent_tip').show();
	},function(){										// 隐藏
		$(this).find('.cotent_tip').hide();
	});
	$('.cotent_items li .cotent_tip').mouseover(function(){
		$(this).show();
	})

	// logo 上传
	options ={
		thumbBox: '.thumbBox',
		spinner: '.spinner',
		imgSrc: imgUrl + 'images/default.png',
		img :  '',
		flag: false
	}

	$('#files').on('change', function(){
		var that = $(this);
		var reader = new FileReader();
		reader.readAsDataURL(this.files[0]); 
		reader.onload = function(e){ 
			that.parents('.shop_logo').children('img').attr('src',this.result);
		}
		$.ajax({
		    url: '/merchants/myfile/upfile',
		    type: 'POST',
		    cache: false,
		    data: new FormData($('#uploadForm')[0]),
		    processData: false,
		    contentType: false,
		    headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            },
		    success:function(res) {
		    	var id = $('#id').val();
		    	res = JSON.parse(res);
		    	var logo = res.data.FileInfo['path'];

		    	$.post('',{id:id,logo:logo,_token:$('meta[name="csrf-token"]').attr('content')},function(data){
		    		console.log(data);
					window.location.reload();
		    	},'json')
			},
			error:function(){

			}
		})
	});
});
